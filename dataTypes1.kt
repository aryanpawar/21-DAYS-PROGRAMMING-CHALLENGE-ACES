fun main() {

//    Data Types In Kotlin

    val byte: Byte = 8 //8-bit signed integer
    val short: Short = 16 //16-bit signed integer
    val int: Int = 32 //32-bit signed integer
    val long: Long = 64 //64-bit signed integer

    val float: Float = 32.00F //32-bit floating point number
    val double: Double = 64.00 //64-bit floating point number

//    Converting Byte to Double
    println(byte.toDouble()::class)

//    Number Writing Style
    val number : Long = 1_000_000_000
}
